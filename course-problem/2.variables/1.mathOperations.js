function add(v1, v2) {
    return v1 + v2;
}
function subtract(v1, v2) {
    return v1 - v2;
}
function multiply(v1, v2) {
    return v1 * v2;
}
function divide(v1, v2) {
    return v1 / v2;
}
function increment(v1) {
    return ++v1;
}
function decrement(v1) {
    return --v1;
}
function remainder(v1, v2) {
    return 24 % 18;
}

console.log(add(24, 18));
console.log(subtract(24, 18));
console.log(multiply(24, 18));
console.log(divide(24, 18));
console.log(increment(18));
console.log(decrement(24));
console.log(remainder(24, 18));